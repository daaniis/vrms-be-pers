function getRandomInt(min, max) {
  return Math.floor(Math.random() * (max - min + 1)) + min;
}

function getRandomRateEndingWith000(min, max) {
  const baseRate = getRandomInt(min / 1000, max / 1000);
  return baseRate * 1000;
}

function getRandomRates() {
  const numRates = getRandomInt(1, 5);
  let rates = [];
  for (let i = 0; i < numRates; i++) {
    rates.push({
      type_of_service: services[getRandomInt(0, services.length - 1)],
      rate: getRandomRateEndingWith000(1000, 10000000),
      rate_type_id: getRandomInt(1, 20).toString(),
      calc_unit: units[getRandomInt(0, units.length - 1)],
    });
  }
  return rates;
}

